    public boolean isPalindrome(ListNode head) {
        if(head == null || head.next == null) return true;

        ListNode current = head;
        Stack<Integer> stack = new Stack<>();

        while (current != null) {
            stack.add(current.val);
            current = current.next;
        }

        current = head;

        while (current != null) {
            if(current.val != stack.pop()) {
                return false;
            }
            current = current.next;
        }

        return true;
    }
